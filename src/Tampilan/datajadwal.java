    protected void datatable(){
        Object[] baris = {"Kd Jadwal ","Nama Jadwal","Hari","Waktu"};
        tabmode = new DefaultTableModel (null, baris);
        tabeljadwal.setModel(tabmode);
        String sql="select*from datajadwal";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("Kd Jadwal");
                String b = hasil.getString("Nama Jadwal");
                String c = hasil.getString("Hari");
                String d = hasil. getString("Waktu");
                String[]data= {a,b,c,d};
                tabmode.addRow(data);
            }
        }catch (Exception e){
            
        }
    }
