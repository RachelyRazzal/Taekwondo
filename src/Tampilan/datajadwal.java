    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String sql = "insert into datajadwal values (?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kode.getSelectedItem().toString());
            stat.setString(2, nama.getText());
            stat.setString(3, hari.getText());
            stat.setString(4, waktu.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            nama.requestFocus();
            datatable();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_saveActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "update datajadwal set `Kd Jadwal`=?,`Hari`=?,`Waktu`=? where `Nama Jadwal`=?";
            PreparedStatement stat= conn.prepareStatement(sql);
            stat.setString(4, nama.getText());
            stat.setString(1, kode.getSelectedItem().toString());
            stat.setString(2, hari.getText());
            stat.setString(3, waktu.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            nama.requestFocus();
            datatable();
            
        }catch (SQLException e){
          JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
           //e.printStackTrace();
        }
    }//GEN-LAST:event_editActionPerformed

